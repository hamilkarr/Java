package Chapter5;

public class Calendar2 {
  private int year;
  private int month;
  private int day;

  public int getYear() {
    return year;
  }

  public void setDay(int day) {
    this.day = day;
  }

  public int getMonth() {
    return month;
  }

  public void setMonth(int month) {
    this.month = month;
  }

  public void setYear(int year) {
    this.year = year;
  }

  public int getDay() {
    return day;
  }
}
